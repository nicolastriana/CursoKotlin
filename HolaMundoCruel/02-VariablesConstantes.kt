/*
 *Programa de Variables y Constantes
 */

fun main(args: Array<String>) {
    
    // Variable
    
    var edadFirulais = 18 
    
    edadFirulais = 15
    
    println(edadFirulais)
    
    // Constante
    
    val nombreFirulais = "Pinky Firulais"
    
    // Error -> nombreFirulais = "Juan"
    
    println(nombreFirulais)
    
}

/*
 *Variable -> Es un contenedor en el espacio de memoria del programa que puede variar con el paso del programa
 *Constante -> Es un contenedor en el espacio de memoria del programa que puede almacenar un dato pero siempre va a mantener el mismo dato una vez almacenado
 */

